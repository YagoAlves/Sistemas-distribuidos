package br.ufc.model;

public class Jogador {
	
	private String nome;
	private String posiçao;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getPosiçao() {
		return posiçao;
	}
	public void setPosiçao(String posiçao) {
		this.posiçao = posiçao;
	}
	
	
}

